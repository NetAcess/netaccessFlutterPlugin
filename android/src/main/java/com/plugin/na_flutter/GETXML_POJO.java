package com.plugin.na_flutter;

public class GETXML_POJO {
    private String sno;
    private String xml;


    public GETXML_POJO() {

    }


    public GETXML_POJO(String sno, String xml) {
        super();
        this.sno = sno;
        this.xml = xml;
    }


    public String getSno() {
        return sno;
    }


    public void setSno(String sno) {
        this.sno = sno;
    }


    public String getXml() {
        return xml;
    }


    public void setXml(String xml) {
        this.xml = xml;
    }


}